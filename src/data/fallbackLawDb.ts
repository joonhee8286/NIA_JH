export function findFallbackLaw(query: string): { articles: LawArticle[]; precedents: LawPrecedent[] } {
  const normalized = query.toLowerCase();
  
  // 간단한 형태소/단어 매칭
  const matchedArticles = fallbackArticles.filter(art => {
    return art.keywords.some(word => normalized.includes(word)) ||
           art.actName.toLowerCase().includes(normalized) ||
           art.title.toLowerCase().includes(normalized) ||
           art.content.toLowerCase().includes(normalized);
  });

  const matchedPrecedents = fallbackPrecedents.filter(prec => {
    return prec.keywords.some(word => normalized.includes(word)) ||
           prec.caseName.toLowerCase().includes(normalized) ||
           prec.summary.toLowerCase().includes(normalized);
  });

  return {
    articles: matchedArticles,
    precedents: matchedPrecedents
  };
}
